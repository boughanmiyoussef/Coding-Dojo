
class redBeltNinja{
    constructor (cost=3,power=3,resilience=4){
        this.cost=cost
        this.power=power
        this.resilience=resilience
    }
    hardAlgorithm(){
        this.cost=2;
        this.stat=resilience;
        this.magnitude=+3;
        console.log("increase target's resilience by 3")
    }

    unhandledPromise(){
        this.cost=1;
        this.stat=resilience;
        this.magnitude=-2;
        console.log("increase target's resilience by 2")
    }

    pairProgramming(){
        this.cost=3;
        this.stat=resilience;
        this.magnitude=+2;
        console.log("increase target's power by 2")
    }
    

}    
const abc= new redBeltNinja();
abc.hardAlgorithm();
abc.unhandledPromise();
abc.pairProgramming();







